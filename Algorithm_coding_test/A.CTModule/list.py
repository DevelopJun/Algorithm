# 리스트 메서드

# 변수명.append() => 리스트에 원소 삽입 / 시간복잡도 : O(1)

list = [2, 8, 4, 1]
list.append(0)
list  # [2, 8, 4, 1, 0]

#########################################################

list.sort()
list  # [0, 1, 2, 4, 8]

list.sort(reverse=True)
list  # [8, 4, 2, 1, 0]

#########################################################
list = [8, 4, 2, 1, 0, 7]
list.reverse()

#########################################################
list = [7, 0, 1, 2, 4, 8]

list.insert(0, 9)
list #[9, 7, 0, 1, 2, 4, 8]

list = [8, 4, 2, 1, 0, 7]
list.insert(1, 9) # [8, 9, 4, 2, 1, 0, 7]
#########################################################
list.append(9)
list # [9, 7, 0, 1, 2, 4, 8, 9]
list.count(9) # 2

